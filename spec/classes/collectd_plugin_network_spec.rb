require 'spec_helper'

describe 'collectd::plugin::network', :type => :class do

  context ':ensure => present, default params' do
    let :facts do
      {:osfamily => 'RedHat'}
    end
    it 'Will create /etc/collectd.d/10-network.conf' do
      should contain_file('network.load').with({
        :ensure  => 'present',
        :path    => '/etc/collectd.d/10-network.conf',
        :content => /\#\ Generated by Puppet\nLoadPlugin network\n\n/,
      })
    end
  end

  context ':ensure => present, TTL to 42' do
    let :facts do
      {:osfamily => 'RedHat'}
    end
    let :params do 
      {:timetolive => '42'}
    end
    it 'Will create /etc/collectd.d/10-network.conf' do
      should contain_file('network.load').with({
        :ensure  => 'present',
        :path    => '/etc/collectd.d/10-network.conf',
        :content => /<Plugin network>\n  TimeToLive 42\n<\/Plugin>\n/,
      })
    end
  end

  context ':ensure => absent' do
    let :facts do
      {:osfamily => 'RedHat'}
    end
    let :params do
      {:ensure => 'absent'}
    end

    it 'Will not create /etc/collectd.d/10-network.conf' do
      should contain_file('network.load').with({
        :ensure => 'absent',
        :path   => '/etc/collectd.d/10-network.conf',
      })
    end
  end
end

